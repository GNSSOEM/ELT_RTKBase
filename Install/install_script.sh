     ExitCodeCheck $?

     #echo systemctl enable --now "${GNSS_WEB_PROXY}"
     systemctl enable --now "${GNSS_WEB_PROXY}"
     ExitCodeCheck $?
  fi

  MOBILE=/sys/class/net/mobile
  if [[ -L ${MOBILE} ]]; then
     #echo systemctl enable --now "${MODEM_WEB_PROXY_SERVICE}"
     systemctl enable --now "${MODEM_WEB_PROXY_SERVICE}"
     ExitCodeCheck $?
