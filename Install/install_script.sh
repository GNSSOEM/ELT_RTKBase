  DHCP_enabled=$(systemctl is-enabled "${DHCP_SERVICE}")
  [[ "${DHCP_enabled}" != "disabled" ]] && [[ "${DHCP_enabled}" != "masked" ]] && systemctl disable "${DHCP_SERVICE}"

  GNSS_WEB_PROXY=rtkbase_gnss_web_proxy.service
  if [[ "${receiver}" =~ Septentrio ]]; then
