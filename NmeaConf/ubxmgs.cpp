#define UBX_CFG_RECORD_SIZE (sizeof(UbxCfgTable[0]))
#define UBX_CFG_TABLE_CNT (sizeof(UbxCfgTable)/UBX_CFG_RECORD_SIZE)
//---------------------------------------------------------------------------
static int UbxCfg_CompareStr(const void *A, const void *B){
   const TUbxCfg *a = (const TUbxCfg *)A;
   const TUbxCfg *b = (const TUbxCfg *)B;
   int res = strcmp(a->name, b->name);
   if (res < 0) res = -1;
   if (res > 0) res = 1;
   return res;
};
//---------------------------------------------------------------------------
void ubx_cfg_sort_check(void)
{
   int k=0;
   for (unsigned i=1; i < UBX_CFG_TABLE_CNT; i++) {
      int res = UbxCfg_CompareStr(&UbxCfgTable[i-1], &UbxCfgTable[i]);
      if (res >= 0) {
         printf(" %s >= %s\n", UbxCfgTable[i-1].name, UbxCfgTable[i].name);
         k++;
      }
   }
   printf("%d errors\n", k);
};
//---------------------------------------------------------------------------
TUbxKey ubx_find_cfg_byStr(const char *str)
{
   //ubx_cfg_sort_check();
   TUbxCfg etalon = {str,0,"",0,"",""};
   TUbxCfg *found = (TUbxCfg *)bsearch(&etalon, UbxCfgTable, UBX_CFG_TABLE_CNT, UBX_CFG_RECORD_SIZE, UbxCfg_CompareStr);
   if (found == NULL)
      return 0;
   else
      return found->key;
};
//---------------------------------------------------------------------------
static int UbxCfg_CompareKey(const void *A, const void *B){
   const TUbxCfg *a = (const TUbxCfg *)A;
   const TUbxCfg *b = (const TUbxCfg *)B;
   if (a->key < b->key) return -1;
   if (a->key > b->key) return 1;
   return 0;
};
//---------------------------------------------------------------------------
const char *ubx_find_cfg_byKey(TUbxKey key)
{
   TUbxCfg etalon = {"",key,"",0,"",""};
   unsigned cnt = UBX_CFG_TABLE_CNT;
   TUbxCfg *found = (TUbxCfg *)lfind(&etalon, UbxCfgTable, &cnt, UBX_CFG_RECORD_SIZE, UbxCfg_CompareKey);
   if (found == NULL) {
      return NULL;
   } else
      return found->name;
};
//---------------------------------------------------------------------------
bool ubx_is_cfg(const char *str)
{
   int res = strncmpi(str,"CFG-",4);
   return res == 0;
}
//---------------------------------------------------------------------------
int ubx_cfg_value_len(TUbxKey key)
{
   int size = key >> 28;
   int vallen;
   switch(size) {
       case 1:
       case 2:  vallen = 1; break;
       case 3:  vallen = 2; break;
       case 4:  vallen = 4; break;
       case 5:  vallen = 8; break;
       default: vallen = 0; break;
   }
   return vallen;
}
