#define UBX_CMD_RECORD_SIZE (sizeof(UbxCmdTable[0]))
#define UBX_CMD_TABLE_CNT (sizeof(UbxCmdTable)/UBX_CMD_RECORD_SIZE)
//---------------------------------------------------------------------------
static int UbxCmd_KeyCompare(const void *A, const void *B){
   const TUbxCmd *a = (const TUbxCmd *)A;
   const TUbxCmd *b = (const TUbxCmd *)B;
   if (a->key < b->key) return -1;
   if (a->key > b->key) return 1;
   return 0;
};
//---------------------------------------------------------------------------
void ubx_cmd_sort_check(void)
{
   int k=0;
   for (unsigned i=1; i < UBX_CMD_RECORD_SIZE; i++) {
      int res = UbxCmd_KeyCompare(&UbxCmdTable[i-1], &UbxCmdTable[i]);
      if (res >= 0) {
         printf("0x%04lX >= 0x%04lX\n", UbxCmdTable[i-1].key, UbxCmdTable[i].key);
         k++;
      }
   }
   printf("%d errors\n", k);
};
//---------------------------------------------------------------------------
char ubx_find_cmd_byKeyStr[20];
const char *ubx_find_cmd_byKey(int key)
{
   //ubx_cmd_sort_check();
   TUbxCmd etalon = {TUbxKey(key),""};
   TUbxCmd *found = (TUbxCmd *)bsearch(&etalon, UbxCmdTable, UBX_CMD_TABLE_CNT, UBX_CMD_RECORD_SIZE, UbxCmd_KeyCompare);
   if (found == NULL) {
      sprintf(ubx_find_cmd_byKeyStr, "UBX-0x%04X", key);
      return ubx_find_cmd_byKeyStr;
   } else
      return found->name;
};
//---------------------------------------------------------------------------
static int UbxCmd_StrCompare(const void *A, const void *B){
   const TUbxCmd *a = (const TUbxCmd *)A;
   const TUbxCmd *b = (const TUbxCmd *)B;
   int res = strcmp(a->name, b->name);
   if (res < 0) res = -1;
   if (res > 0) res = 1;
   return res;
};
//---------------------------------------------------------------------------
TUbxKey ubx_find_cmd_byStr(const char *str)
{
   TUbxCmd etalon = {0,str};
   unsigned cnt = UBX_CMD_TABLE_CNT;
   TUbxCmd *found = (TUbxCmd *)lfind(&etalon, UbxCmdTable, &cnt, UBX_CMD_RECORD_SIZE, UbxCmd_StrCompare);
   if (found == NULL) {
      return 0;
   } else
      return found->key;
};
